template <typename T>
struct Target
{
    T content;
    std::mutex my_mutex;
    bool activate = false;

    void put(const T &data)
    {
        std::lock_guard<std::mutex> guard(my_mutex);
        content = data;   // generic type must be copy-constructable
        activate = true;
    }
    std::optional<T> get()
    {
        std::lock_guard<std::mutex> guard(my_mutex);
        if(activate)
            return content;
        else
            return {};
    }
    void set_task_finished()
    {
        std::lock_guard<std::mutex> guard(my_mutex);
        activate = false;
    }
};
